// Fonctions utilitaires pour les quiz
export const getUserQuizScores = (userId) => {
  const scores = localStorage.getItem(`quiz_scores_${userId}`);
  return scores ? JSON.parse(scores) : {};
};

export const getBestScore = (quizId, userId) => {
  const scores = getUserQuizScores(userId);
  return scores[quizId] ? Math.max(...scores[quizId]) : 0;
};

export const saveQuizScore = (quizId, score, userId) => {
  const scores = getUserQuizScores(userId);
  if (!scores[quizId]) {
    scores[quizId] = [];
  }
  scores[quizId].push(score);
  localStorage.setItem(`quiz_scores_${userId}`, JSON.stringify(scores));
};

